export const findPhoneCountry = (label: string) => phoneCountries.find((country) => country.label === label);

export const searchPhoneCountries = (query: string) => {
  const normalized = query.trim().toLowerCase();
  if (!normalized) return phoneCountries;

  return phoneCountries.filter((country) =>
    country.name.toLowerCase().includes(normalized) ||
    country.label.toLowerCase().includes(normalized) ||
    country.code.toLowerCase().includes(normalized)
  );
};
